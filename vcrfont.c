static PIXEL_T *vcr_bitmap_for_code(int c) {
  for (PIXEL_T *p = vcrfont; p < vcrfont + sizeof(vcrfont)/sizeof(PIXEL_T); p += VCR_FONT_H + 1) {
    if (*p == c) return p + 1;
  }
  return vcrfont + 1;
}
static int utf8_char_len(char c) {
  if (c < 0x80) return 1;
  if (c < 0xE0) return 2;
  if (c < 0xF0) return 3;
  return 4;
}
static int utf8_get_char(const char *c) {
  if (*c < 0x80) return *c;
  if (*c < 0xE0) return ((*c & 0x1F) << 6)+ (c[1]&0x3F);
  if (*c < 0xF0) return ((*c & 0xF) << 12)+ ((c[1]&0x3F) << 6) + (c[2]&0x3F);
  return ((*c & 0x7) << 18) + ((c[1]&0x3F) << 12) + ((c[2]&0x3F) << 6) + (c[3]&0x3F);
}
static void render_char(int x, PIXEL_T *b, void (*put_pixel)(int x, int y, int v)) {
  for (int i = 0; i < VCR_FONT_H; i++) {
    int line = b[i];
    for (int j = 0; j < VCR_FONT_W; j++) {
      put_pixel(x * VCR_FONT_W + j, i, (line >> (VCR_FONT_W - j - 1)) & 1);
    }
  }
}
int render_text(const char *s, void (*put_pixel)(int x, int y, int v), int max_width) {
  int l = strlen(s);
  int i, x;
  for (i = x = 0; i < l && x < max_width / VCR_FONT_W; x++) {
    int j = utf8_char_len(s[i]);
    if (i + j > l) break;
    int c = utf8_get_char(s + i);
    PIXEL_T *b = vcr_bitmap_for_code(c);
//    printf("Rendering code %d at %d bitmap %d\n", c, i, (b - vcrfont) / 18);
    render_char(x, b, put_pixel);
    i += j;
  }
  return x * VCR_FONT_W;
}
